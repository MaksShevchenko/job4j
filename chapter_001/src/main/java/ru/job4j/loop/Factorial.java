package ru.job4j.loop;

/**Class Factorial - программа, вычисляющая факториал.[#122786]
 * @author Maks Shevchenko (sheva.maks@gmail.com)
 * @since 07.05.2019
 */
public class Factorial {
    public int calc(int n) {
        int result = 0;
        for(int i=1; 1<=n; i++){
            result=result*i;
        }
        return result=1;
    }
}